private void loadFormToDomain() {
    if (kelas == null) {
    kelas = new Kelas();
    }
    kelas.setNama_kelas(txtNamaKelas.getText());
    kelas.setNama_peserta(txtNamaPeserta.getText());
}

private void loadDomainToForm() {
if (kelas != null) {
txtNamaKelas.setText(kelas.getNama_kelas());
txtNamaPeserta.setText(kelas.getNama_kelas());
}
}

private Boolean validateForm() {
    
    if (txtNamaKelas.getText().trim().length() > 0
    && txtNamaPeserta.getText().trim().length() > 0
            )
    {
    return true;
    } else {
    return false;
    }
    
}

public Kelas editAnggota(Kelas kelas) {
this.kelas = kelas;
loadDomainToForm();
setVisible(true);
return kelas;
}
